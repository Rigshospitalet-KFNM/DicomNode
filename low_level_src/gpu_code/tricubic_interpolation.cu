template<typename T>
__global__ void tricubic_interpolations(T* image,
                                        uint32_t x_max,
                                        uint32_t y_max,
                                        uint32_t z_max,
                                        float minimum_value){
  extern __shared__ volatile T shared_volume[];

  ImageDimensions image_dimensions{x_max, y_max, z_max};
